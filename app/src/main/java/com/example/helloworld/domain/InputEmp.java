package com.example.helloworld.domain;

public class InputEmp {
    String email;
    String password;

    public InputEmp() {
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
}
